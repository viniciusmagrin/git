options asked by git when installing in windows:

check: add a git bash profile to windows terminal
text editor: use notepad++
name of the initial branch: override to main
ssh: use bundled (the ssh that comes with git)
https transport backend: use openssl library
line ending conversions: checkout windows style, commit unix style line endings
terminal emulator: use mintty
default behavior of git pull: merge
credential helper: Git credential manager
extra options: enable file system caching